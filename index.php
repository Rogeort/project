    <h2>EL PROBLEMA</h2>
    <p>Principalmente partimos a definir las condiciones geográficas del lugar donde ocurre
el problema. En Cortázar el clima es semicálido húmedo con lluvias en verano,
crecen diversas especies forrajeras, tales como navajita, zacatón, pata de gallo,
flechilla, tres barbas, lanudo y tempranero. También hay huizaches, nopales y
largoncillo. El mezquite es una especie muy común en toda la región. El suelo tiene
una estructura blocosa angular y consistencia muy firme a firme, con textura de
arcillo limosa a arcillo arenosa y pH de 6.8 a 8.9 de origen aluvión coluvial. El 72,16
% de la superficie municipal se destina a actividades de carácter agrícola, el 5,16 %
a pastizales; el 0,21 % a bosques y el 23 % a matorrales. Las principales fuentes de
contaminación son el vertido de aguas residuales en el Río Laja y Canal (Dren)
Merino generando contaminación al medio ambiente por agroquímicos y aguas
residuales que son vertidas sin tratamiento previo, además esta agua se utiliza para
riego, añadiendo la contaminación del aire por agroquímicos y quema de la pata de
sorgo afectando además la visibilidad de los conductores en las vías de
comunicación.</p>
